let saldo = 500

function somar(valor) {
    return saldo + valor
}

function sub(valor) {
    return saldo - valor
}

function mult(valor) {
    return saldo * valor
}

function div(valor) {
    return saldo / valor
}

console.log(somar(10))
console.log(sub(10))
console.log(mult(10))
console.log(div(10))

